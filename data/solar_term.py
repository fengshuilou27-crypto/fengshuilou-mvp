def get_lichun_date(year: int) -> tuple:
    """
    獲取指定年份的立春日期 (month, day)
    超出查表範圍默認返回 (2, 4)
    """
    return LICHUN_DATES.get(year, (2, 4))


def is_before_lichun(month: int, day: int, year: int) -> bool:
    """
    判斷指定日期是否在立春之前
    
    Args:
        month: 月份 (1-12)
        day: 日期 (1-31)
        year: 年份
    
    Returns:
        True: 在立春前（應使用上一年計算命卦）
        False: 在立春後（應使用當年計算命卦）
    """
    lichun_month, lichun_day = get_lichun_date(year)
    
    # 如果月份小於立春月，必定在立春前
    if month < lichun_month:
        return True
    # 如果月份等於立春月，比較日期
    if month == lichun_month and day < lichun_day:
        return True
    # 其他情況在立春後
    return False


def get_lunar_year_for_ming_gua(birth_date: str) -> int:
    """
    根據出生日期獲取用於命卦計算的年份
    
    傳統命理以立春為年界：
    - 立春前出生：使用上一年（上一年干支）
    - 立春後出生：使用當年（當年干支）
    
    Args:
        birth_date: 格式 "YYYY-MM-DD"
    
    Returns:
        調整後的年份（用於命卦計算）
    
    Example:
        >>> get_lunar_year_for_ming_gua("1991-01-15")  # 1991立春=2月4日
        1990  # 1月15日在立春前，使用1990年（庚午）
        >>> get_lunar_year_for_ming_gua("1991-05-20")  # 5月20日在立春後
        1991  # 使用當年（辛未）
    """
    try:
        parts = birth_date.split("-")
        if len(parts) != 3:
            # 格式錯誤，返回原年份
            return int(birth_date.split("-")[0])
        
        year = int(parts[0])
        month = int(parts[1])
        day = int(parts[2])
        
        if is_before_lichun(month, day, year):
            return year - 1
        return year
        
    except (ValueError, IndexError):
        # 解析失敗，返回原年份
        return int(birth_date.split("-")[0]) if "-" in birth_date else int(birth_date)


